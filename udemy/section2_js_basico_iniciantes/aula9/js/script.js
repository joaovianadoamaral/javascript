// peça dois números no prompt
// mostre o resultado da soma usando alert
let num1 = window.prompt('Digite um número:');
let num2 = window.prompt('Digite outro número: ');

num1 = Number(num1)
num2 = Number(num2)

const somaNum1Num2 = num1 + num2;

alert(`O resultado da sua conta foi: ${somaNum1Num2}`);
